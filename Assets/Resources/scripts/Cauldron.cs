using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Cauldron : MonoBehaviour {
    /* <class> Cauldron </class>
     * <description> Tells the gamecontroller that the player has submitted
     * the proper ingredient, the gamecontroller should then update the UI's
     * recipe ingredients list display, as well as it's own tracking system.
     * </description>
     * 
     * <variables> </variables>
     */
    public List<GameObject> ingredients;
    void OnMouseDown()
    {
        Familiar f = GameController.familiar;
        if (f.ingredient != null && !f.Busy())
        {            
            f.Deliver(this);
        }
        GameController.AddTurn();
    }
    public void Deliver(GameObject ingredient)
    {
        if (ingredient == null) return;
        GameController.CheckRecipe(ingredient);
        Destroy(ingredient);
    }

}
